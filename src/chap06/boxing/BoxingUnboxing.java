package chap06.boxing;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.LinkedHashSet;
import java.util.List;
import java.util.PriorityQueue;
import java.util.TreeSet;

public class BoxingUnboxing {

	static Integer v; // null by default

	public static void main(String[] args) {
		int x = v; // throws NPE at unbox time when assigning v to x,
					// because v does not contain a value to unbox
		int a = 10;
		System.out.println(x + a); // line unreachable because of NPE

		Integer i1 = 10; // Autoboxing
		Integer i2 = 10; // Autoboxing
		int i = i1 + i2;// Unboxing
		System.out.println(i);
	}
}
